mark=int(input("Enter your mark:"))
if mark<35:
 print("Gerad F: Fail")

elif mark<=35 and mark<=40:
 print("Grade D")

elif mark<=41 and mark<50:
 print("Grade C")

elif mark>=50 and mark<60:
 print("Grade B")

elif mark<=60 and mark<70:
 print("Grade B+")

elif mark>=70 and mark<80:
 print("Grade D")

else:
 print("Grade A+")